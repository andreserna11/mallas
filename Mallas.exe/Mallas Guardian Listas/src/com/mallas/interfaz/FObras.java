    private void jLabelGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelGuardarMouseClicked
        EntObras cabeza = null;
        obraE.setNext(cabeza);

        /**
         *Se realizan las validaciones de los campos de texto*/
        
        if (txtReferencia.getText().trim().length() > 0) {
            obraE.setReferencia(txtReferencia.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la Referencia de la obra", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtNombre.getText().trim().length() > 0) {
            obraE.setNombre(txtNombre.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el Nombre de la obra", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtCliente.getText().trim().length() > 0) {
            obraE.setNombre_cliente(txtCliente.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el Nombre de la obra", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (calendarFI.getDateFormatString().trim().length() > 0) { //trim= quitar espacios en blanco, length= contar los espacios.
            int año = calendarFI.getCalendar().get(java.util.Calendar.YEAR);
            int mes = calendarFI.getCalendar().get(java.util.Calendar.MONTH) + 1;
            int dia = calendarFI.getCalendar().get(java.util.Calendar.DATE);
            String fechaI = dia + "/" + mes + "/" + año;
            obraE.setFecha_inicio(fechaI); //sacar informacion del cuadro de texto para meterlo en insumo. 

        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la Fecha de Inicio", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (calendarFF.getDateFormatString().trim().length() > 0) {
            int año = calendarFF.getCalendar().get(java.util.Calendar.YEAR);
            int mes = calendarFF.getCalendar().get(java.util.Calendar.MONTH) + 1;
            int dia = calendarFF.getCalendar().get(java.util.Calendar.DATE);
            String fechaF = dia + "/" + mes + "/" + año;
            obraE.setFecha_final(fechaF); //sacar informacion del cuadro de texto para meterlo en insumo. 

        }               

        obraE.setCiudad(cboCiudad.getSelectedItem().toString());

        if (txtDireccion.getText().trim().length() > 0) {
            obraE.setDireccion(txtDireccion.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la Dirección de la obra", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        obraE.setEstado(cboEstado.getSelectedItem().toString());

        boolean respuesta = obraN.guardarObraEnt(obraE);

        if (respuesta) {
            JOptionPane.showMessageDialog(null, "Se ha guardado satisfactoriamente una obra en el sistema", "Guardado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "La obra ya existe", "Guardado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelGuardarMouseClicked

