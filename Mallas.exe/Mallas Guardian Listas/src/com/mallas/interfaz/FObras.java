    private void jLabelConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelConsultarMouseClicked
        
        /**
         *Se realiza la validación de la referencia de la obra*/
         
        String referencia = JOptionPane.showInputDialog(this, "Por favor ingrese la Referencia de la obra", "Consultar", JOptionPane.INFORMATION_MESSAGE); //Ventana para ingresar texto        
        obraE = obraN.consultarObraEnt(referencia);

        /**
         * Se llenan los campos de texto con los respectivos datos*/
        
        if (obraE != null) {
            txtReferencia.setText(obraE.getReferencia());
            txtNombre.setText(obraE.getNombre());
            txtCliente.setText(obraE.getNombre_cliente());
            calendarFI.setDateFormatString(obraE.getFecha_inicio());
            calendarFF.setDateFormatString(obraE.getFecha_final());
            cboCiudad.setSelectedItem(obraE.getCiudad());
            txtDireccion.setText(obraE.getDireccion());
            cboEstado.setSelectedItem(obraE.getEstado());

            txtReferencia.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this, "La obra no se ha encontrado", "Consultar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelConsultarMouseClicked

