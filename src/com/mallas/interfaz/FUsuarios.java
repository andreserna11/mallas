    private void jLabelConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelConsultarMouseClicked
        int dni = Integer.parseInt(JOptionPane.showInputDialog(this, "Por favor ingrese el documento de identidad del usuario", "Consultar", JOptionPane.INFORMATION_MESSAGE)); //Ventana para ingresar texto        
        userE = userN.consultarUsuarioEnt(dni);

        if (userE != null) {
            txtUsuario.setText(userE.getUsuario());
            txtClave.setText(userE.getClave());
            cboPerfil.setSelectedItem(userE.getRol());
            cboEstado.setSelectedItem(userE.getEstado());
            txtDNI.setText(Integer.toString(userE.getDNI()));
            txtNombre.setText(userE.getNombre());
            txtApellido.setText(userE.getApellido());
            txtCorreo.setText(userE.getCorreo());

            txtUsuario.setEnabled(false);
            txtDNI.setEnabled(false);
            txtNombre.setEnabled(false);
            txtApellido.setEnabled(false);

        } else {
            JOptionPane.showMessageDialog(this, "El usuario no se ha encontrado", "Consultar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelConsultarMouseClicked

