    private void jLabelConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelConsultarMouseClicked
        
        String cedula = JOptionPane.showInputDialog(this, "Por favor ingrese la cédula del empleado", "Consultar", JOptionPane.INFORMATION_MESSAGE);
        userE = userN.consulEntEmpleado(cedula);
        
        if (userE != null) {
            txtCedula.setText(userE.getCedula());
            txtNombre.setText(userE.getNombre());
            txtApellido.setText(userE.apellido);
            txtEdad.setText(userE.getEdad());
            txtTelefono.setText(userE.getTelefono());
            txtDireccion.setText(userE.getDireccion());
            
            txtCedula.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this, "El empleado no se ha encontrado", "Consultar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelConsultarMouseClicked

