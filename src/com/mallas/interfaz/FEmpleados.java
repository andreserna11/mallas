    private void jLabelGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelGuardarMouseClicked
        EntEmpleado cabeza = null;
        userE.setNext(cabeza);
        
        if (txtCedula.getText().trim().length() > 0){
            userE.setCedula(txtCedula.getText());
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese la Cédula", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (txtNombre.getText().trim().length() > 0){
            userE.setNombre(txtNombre.getText());
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese el Nombre", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (txtApellido.getText().trim().length() > 0){
            userE.setApellido(txtApellido.getText());
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese el Apellido", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (txtEdad.getText().trim().length() > 0){
            userE.setEdad(txtEdad.getText());
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese la edad", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (txtTelefono.getText().trim().length() > 0){
            userE.setTelefono(txtTelefono.getText());
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese el Teléfono", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (txtDireccion.getText().trim().length() > 0){
            userE.setDireccion(txtDireccion.getText());
        } else {
            JOptionPane.showMessageDialog(null,"Por favor ingrese el Dirección", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        boolean respuesta = userN.guardarEmpleado(userE);
        
        if (respuesta == true) {
            JOptionPane.showMessageDialog(null, "Se ha guardado satisfactoriamente un empleado en el sistema", "Guardado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "El empleado ya existe", "Guardado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelGuardarMouseClicked

