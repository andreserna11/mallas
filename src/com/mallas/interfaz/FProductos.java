    private void jLabelGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelGuardarMouseClicked
        EntProductos cabeza = null;
        prodE.setNext(cabeza);

        if (txtCodigo.getText().trim().length() > 0) {
            prodE.setCodigo_producto(txtCodigo.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el Código del Producto", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtNombre.getText().trim().length() > 0) {
            prodE.setNombre(txtNombre.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el Nombre del Producto", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int precio = Integer.parseInt(txtPrecio.getText());

        if (txtPrecio.getText().trim().length() > 0) {
            prodE.setPrecio(precio);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el Precio del Producto", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int stock = Integer.parseInt(txtStock.getText());

        if (txtStock.getText().trim().length() > 0) {
            prodE.setStock(stock);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la Cantidad en Stock del Producto", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtDescripcion.getText().trim().length() > 0) {
            prodE.setDescripcion(txtDescripcion.getText());
        } else {
            prodE.setDescripcion("Sin Descripción");
        }

        int cantidad = (int) spCantidad.getValue();

        if (cantidad > 0) {
            prodE.setCantidad(cantidad);
        } else {
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(null, "La Cantidad a almacenar del Producto debe ser mayor a 0 (cero)", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "Por favor ingrese la Cantidad a almacenar del Producto", "Datos Faltantes", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        boolean respuesta = prodN.guardarProducto(prodE);
        
        if(respuesta){
            JOptionPane.showMessageDialog(null, "Se ha guardado satisfactoriamente un producto en el sistema", "Guardado", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "El producto ya existe", "Guardado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelGuardarMouseClicked

