    private void jLabelConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelConsultarMouseClicked
        String codigo = JOptionPane.showInputDialog(this, "Por favor ingrese el código del producto", "Consultar", JOptionPane.INFORMATION_MESSAGE);
        prodE = prodN.consutlarProducto(codigo);
        
        if(prodE != null){
            txtCodigo.setText(prodE.getCodigo_producto());
            txtNombre.setText(prodE.getNombre());
            txtPrecio.setText(Integer.toString(prodE.getPrecio()));
            txtStock.setText(Integer.toString(prodE.getStock()));
            txtDescripcion.setText(prodE.getDescripcion());
            spCantidad.setValue(prodE.getCantidad()); 
            
            txtCodigo.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(this, "El producto no se ha encontrado", "Consultar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabelConsultarMouseClicked

